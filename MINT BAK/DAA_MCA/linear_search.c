#include <stdio.h>
int main() 
{
int n, i, key;
printf("Enter size of array: ");
scanf("%d", &n);
int a[n];
printf("Enter %d numbers:\n", n);
for (i = 0; i < n; i++)
    scanf("%d", &a[i]);
printf("Enter number to search: ");
scanf("%d", &key);
for (i = 0; i < n; i++)
 {
    if (a[i] == key) 
	{
    printf("Number found at position %d\n", i + 1);
    return 0;
    }
    }
    printf("Number not found\n");
    return 0;
}

